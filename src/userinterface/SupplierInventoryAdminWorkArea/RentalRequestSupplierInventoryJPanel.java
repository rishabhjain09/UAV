    private void btnProcessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblInventory.getSelectedRow();
        
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a row");
            return;
        }
        
        RentalWorkRequest request = (RentalWorkRequest)tblInventory.getValueAt(selectedRow,2);
     
        request.setStatus("Processing");
        RentalProcessSupplierInventoryJPanel rentalprocesssupplierinventory = new RentalProcessSupplierInventoryJPanel(container,account,organization,enterprise,business,request);
        container.add("RentalProcessSupplierInventoryJPanel", rentalprocesssupplierinventory);
        CardLayout layout = (CardLayout) container.getLayout();
        layout.next(container);
    }//GEN-LAST:event_btnProcessActionPerformed

