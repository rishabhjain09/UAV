     public void populateTable(){
        System.out.println("OQQQRRGGAA"+organization);
        DefaultTableModel dtm = (DefaultTableModel)tblInventory.getModel();
        dtm.setRowCount(0);
        for(WorkRequest request: organization.getWorkQueue().getWorkRequestList()){
           if(request.getRentalorrender().equals("Rental")){
                Object[] row1 = new Object[8];
                row1[0]=request.getSupplier();
                row1[1]=request.getProduct();
                row1[2]=request;
                row1[3]=request.getQuantity();
                row1[4]=request.getSender().getEmployee().getName();
                row1[5]=request.getReceiver() == null ? null : request.getReceiver().getEmployee().getName();;
                row1[6]=request.getFrom();
                row1[7]=request.getTo();
                dtm.addRow(row1);
           }
        }
    }
