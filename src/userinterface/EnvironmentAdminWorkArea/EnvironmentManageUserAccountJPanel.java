    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        Organization organization1 = (Organization) comboOrganization.getSelectedItem();
        Employee employee = (Employee) comboEmployee.getSelectedItem();
        Role role = (Role) comboRole.getSelectedItem();
        String userName = txtUserName.getText();
        String password = String.valueOf(pwdpassword.getPassword());
        if((userName != null)&&!(userName.isEmpty()) && (password != null)&&!(password.isEmpty()) && (organization1 != null) && (employee != null) && (role != null))
        {
            Pattern pattern = Pattern.compile("[^A-Za-z0-9]");
            Matcher m = pattern.matcher(userName+password);
            boolean boo = m.find();
            if(boo == true || userName == null || password == null) {
                JOptionPane.showMessageDialog(null,"Enter Valid Details");
            }
            else{  
                UserAccount userAccount = organization1.getUserAccountDirectory().createUserAccount(userName, password, employee, role);
                populateTable();
            }
        }
        else {
            JOptionPane.showMessageDialog(null,"Enter Valid Details");
        }      
    }//GEN-LAST:event_btnCreateActionPerformed

