    private void btnGraphActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGraphActionPerformed
        // TODO add your handling code here:
        float avgtemp =0,avgwindspeed=0,avghumidity=0;
        float avgtemperature1 =0,avghumidity1 =0,avgwindspeed1 =0;
        int count=0;
        Date To = new Date();
        Date From = new Date();
        for(DataWorkRequest data:account.getDataWorkQueue().getDataworkRequestList()){
           To = data.getFrom();
           From = data.getTo();
            avgtemp = avgtemp+data.getTemperature();
            avgwindspeed = avgwindspeed+data.getWindspeed();
            avghumidity = avghumidity + data.getHumidity();
            count ++;
        }
        //   System.out.println("TEMp"+avgtemp+"WIND"+avgwindspeed+"Count"+count);
        avgtemperature1 = (avgtemp/count);
        avgwindspeed1 = (avgwindspeed/count);
        avghumidity1 = (avghumidity/count);
        //System.out.println("TEMp"+avgtemperature1+"WIND"+avgwindspeed1+"Count"+count);
        ShowGraphJPanel   showgraph = new ShowGraphJPanel(container,avghumidity1,avgtemperature1,avgwindspeed1,From,To);
        container.add("ShowGraphJPanel", showgraph);
        CardLayout layout = (CardLayout) container.getLayout();
        layout.next(container);
    }//GEN-LAST:event_btnGraphActionPerformed

